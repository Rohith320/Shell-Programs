    read -p &quot;Enter a string: &quot; s
    reverse=&quot;&quot;
    len=${#s}
    for (( i=$len-1; i&gt;=0; i-- ))
    do 
        reverse=&quot;$reverse${s:$i:1}&quot;
    done
    if [ &quot;$s&quot; = &quot;$reverse&quot; ]
    then
    echo &quot;The string is palindrome&quot;
    else
